# Calcular el total a pagar en una compra

# Ingresamos el precio unitario del producto
precio_unitario = float(input("Ingrese el precio del producto: "))

# Ingresamos la cantidad de productos comprados
cantidad = int(input("Ingrese la cantidad comprada: "))

# Calculamos el total multiplicando precio por cantidad
total_pagar = precio_unitario * cantidad

# Mostramos el total a pagar
print("Total a pagar:", total_pagar)
