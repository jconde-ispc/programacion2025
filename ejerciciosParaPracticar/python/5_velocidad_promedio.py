# Calcular la velocidad promedio

# Definimos la distancia de la maratón (en km)
DISTANCIA = 10

# Ingresamos el tiempo en minutos que tardó la persona
tiempo = float(input("Ingrese el tiempo en minutos: "))

# Calculamos la velocidad dividiendo distancia por tiempo
velocidad_promedio = DISTANCIA / tiempo

# Mostramos la velocidad promedio
print("La velocidad promedio es:", velocidad_promedio, "km/minuto")
